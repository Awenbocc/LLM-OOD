    @torch.no_grad()
    def compute_ood(
            self,
            input_ids=None,
            attention_mask=None,
            labels=None,
    ):      
        transformer_outputs = self.model(
                input_ids=input_ids,
                attention_mask=attention_mask,
            )
        hidden_states = transformer_outputs[0] 
        # logits, pooled = self.classifier(sequence_output)
        if self.config.sentence_emb == 'avg':
            pooled = self.avg_pooling_hidden_states(attention_mask, hidden_states)
        elif self.config.sentence_emb == 'last':
            pooled = hidden_states[:,self.last_pos,:]
        
        logits = self.lm_head(hidden_states) # bsz, len, vocab
        logits = logits[:,-1,:]  # condition on the last token, left padding
        
        
        ood_keys = None
        
        if self.config.task == 'sst2':
            # prob_choices = [9135, 29940]
            prob_choices = [1066, 22198]
        elif self.config.task == '20ng':
            prob_choices = [271, 12097, 1516, 29690, 8628, 
                            10499, 29888, 1300, 14817, 3188, 
                            1251, 29883, 15436, 2168, 3493,
                            305, 29887, 29885, 26737, 29881]
        elif self.config.task == 'clinc150':
            prob_choices = self.prob_loc
        
        assert prob_choices is not None
        
        # softmax score   #        ### Input:\n{sentence}\n\n### Output:\nPositive
        softmax_score = F.softmax(logits, dim=-1)[:, prob_choices].max(-1)[0]
        maha_score = []
        for c in self.all_classes:
            centered_pooled = pooled - self.class_mean[c].unsqueeze(0)
            ms = torch.diag(centered_pooled @ self.class_var @ centered_pooled.t())
            maha_score.append(ms)
        maha_score = torch.stack(maha_score, dim=-1)
        maha_score = maha_score.min(-1)[0]
        maha_score = -maha_score

        norm_pooled = F.normalize(pooled, dim=-1)
        cosine_score = norm_pooled @ self.norm_bank.t()
        cosine_score = cosine_score.max(-1)[0]

        # energy_score
        logits = logits[:, prob_choices]
        energy_score = torch.logsumexp(logits, dim=-1)

        ood_keys = {
            'softmax': softmax_score.tolist(),
            'maha': maha_score.tolist(),
            'cosine': cosine_score.tolist(),
            'energy': energy_score.tolist(),
        }
        return ood_keys

    def prepare_inputs_for_generation(
        self, input_ids, past_key_values=None, attention_mask=None, inputs_embeds=None, **kwargs
    ):
        if past_key_values:
            input_ids = input_ids[:, -1:]

        position_ids = kwargs.get("position_ids", None)
        if attention_mask is not None and position_ids is None:
            # create position_ids on the fly for batch generation
            position_ids = attention_mask.long().cumsum(-1) - 1
            position_ids.masked_fill_(attention_mask == 0, 1)
            if past_key_values:
                position_ids = position_ids[:, -1].unsqueeze(-1)

        # if `inputs_embeds` are passed, we only want to use them in the 1st generation step
        if inputs_embeds is not None and past_key_values is None:
            model_inputs = {"inputs_embeds": inputs_embeds}
        else:
            model_inputs = {"input_ids": input_ids}

        model_inputs.update(
            {
                "position_ids": position_ids,
                "past_key_values": past_key_values,
                "use_cache": kwargs.get("use_cache"),
                "attention_mask": attention_mask,
            }
        )
        return model_inputs

